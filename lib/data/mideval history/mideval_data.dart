  String dynastyName;
  Pair era;
  String about;
  List<Kings> kings ;
  List<Events> events = [];
  MindMap mindmap = MindMap();
  Maps map = Maps();
  Dynasty({required this.dynastyName, required this.era, required this.about, required this.kings});
}
class Kings {
  String kingsName;
  Pair era;
  String about;
  List<Events> events = [];
  Maps map = Maps();
  List<Image> images;
  Kings({required this.kingsName, required this.era, required this.images ,required this.about});
}

